package com.company;

import java.awt.*;

public class Main {

	public static void main(String[] args) {

		// This is an expression
		int /*not including int*/ firstVariable = 20 /*not including semicolon*/;
		System.out.println(firstVariable);
		// These are Statements. Semi-colon is almost always needed to complete a Java statement.
		int myVariable = 50;
		myVariable++;
		System.out.println("This is a complete Java statement.");
	}
}
